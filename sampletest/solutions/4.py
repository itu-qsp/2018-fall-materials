def calculate_volume(length, width, height):
    """Calculates the volume from the given length, width and height.

    Arguments:
    length -- The length of the volume
    width  -- The width of the volume
    height -- The height of the volume

    Return:
    The volume calculated based on the length width and height
    """
    
    return length * width * height
